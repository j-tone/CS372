    private void listRValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_listRValueChanged
        String currR = _rListNames.get(listR.getSelectedIndex());
        File f = new File("C:\\Users\\Justin\\Documents\\GitHub\\CS372\\PR4_1\\" + currR + ".txt");
        String line;
        String format = "";
        try{
            BufferedReader in = new BufferedReader(new FileReader(f));
            while ((line = in.readLine()) != null)
                format += (line + '\n');
                
            in.close();
            txtD.setText(format);
        }
        catch(Exception e){}
    }//GEN-LAST:event_listRValueChanged

