    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        dialogR.setVisible(true);
        buttonAddR.setText("Edit");
        try{
            String currR = _rListNames.get(listR.getSelectedIndex());
            File f = new File("C:\\Users\\Justin\\Documents\\GitHub\\CS372\\PR4_1\\" + currR + ".txt");
            curr_restaurant.readFromFile(f);
            txtName.setText(curr_restaurant.getName());
            txtLocation.setText(curr_restaurant.getLocation());
            txtDescription.setText(curr_restaurant.getDescription());
            switch (curr_restaurant.getStarCount()){
                case 1:
                    rate1.setSelected(true);
                    break;
                case 2:
                    rate2.setSelected(true);
                    break;
                case 3:
                    rate3.setSelected(true);
                    break;
                case 4:
                    rate4.setSelected(true);
                    break;
                case 5:
                    rate5.setSelected(true);
                    break;
                default:
            }
        }
        catch(Exception e){}
    }//GEN-LAST:event_buttonEditActionPerformed

    private void buttonAddRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddRActionPerformed
        try{
            makeReview();
            curr_restaurant.writeToFile();
            ((DefaultListModel)listR.getModel()).addElement(curr_restaurant.getFileName());
            try (BufferedWriter out = new BufferedWriter(new FileWriter(file, true))) {
                out.write(curr_restaurant.getFileName());
                out.newLine();
                out.flush();
                out.close();
            }
            resetFields();
            dialogR.setVisible(false);
        }
        catch(Exception e){System.out.println("OH NOOOOO");}
    }//GEN-LAST:event_buttonAddRActionPerformed
    private void makeReview()throws Exception{
        String name = txtName.getText();
        String location = txtLocation.getText();
        String description = txtDescription.getText();
        int stars;
        if (rate1.isSelected())
            stars = 1;
        else if (rate2.isSelected())
            stars = 2;
        else if (rate3.isSelected())
            stars = 3;
        else if (rate4.isSelected())
            stars = 4;
        else if (rate5.isSelected())
            stars = 5;
        else
            throw new Exception();
        curr_restaurant = new Restaurant(name, location, description, stars);
    }
    private void resetFields(){
        txtName.setText(null);
        txtLocation.setText(null);
        txtDescription.setText(null);
    }
